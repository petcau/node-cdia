console.log("FOr 1 até 10")
for (let i = 1; i <= 10; i++){
    console.log (i);
}

console.log("FOr 10 até 1")
for (let i = 10; i >= 1; i--){
    console.log (i)
}

console.log("While 1 até 10")
i = 1
while (i <= 10){
    console.log (i)
    i++
}

console.log("While 10 até 1")
i = 10
while (i >= 1){
    console.log (i)
    i--
}